function fn = get_result_path(config)
fn = '';
annotation = sprintf('ex(%s)_CR(%s)_SNP(%s)_chr(%s)_gene(%s)_geo(%s)_class(%s)', ...
                    config.exclude,...
                    config.cross_reactive,...
                    config.snp,...
                    config.chr,...
                    config.gene_region,...
                    config.geo,...
                    config.probe_class);
attribute = sprintf('cells(%s)',...
                    config.cells);

obs_keys = config.obs.keys;
for obs_id = 1:size(obs_keys, 2)
    attribute = strcat(attribute, '_', obs_keys{1, obs_id}, '(', config.(obs_keys{1, obs_id}), ')');
end
if strcmp(config.data_type, 'gene') || strcmp(config.data_type, 'bop') || strcmp(config.data_type, 'cpg') 
    fn = sprintf('%s/%s/%s/%s/%s/%s/%s', ...
                    config.data_base, ...
                    config.data_type, ...
                    config.experiment,...
                    config.task,...
                    config.method,...
                    annotation,...
                    attribute);
elseif strcmp(config.data_type, 'attributes')
    fn = sprintf('%s/%s', ...
                    config.data_base, ...
                    config.data_type);
end
end