function fn = get_result_path(config)
fn = '';
annotations = sprintf('ex(%s)_CR(%s)_SNP(%s)_chr(%s)_gene(%s)_geo(%s)_class(%s)', ...
                    config.exclude,...
                    config.cross_reactive,...
                    config.snp,...
                    config.chr,...
                    config.gene_region,...
                    config.geo,...
                    config.probe_class);
                
observables = '';             
obs_keys = config.obs.keys;
for obs_id = 1:size(obs_keys, 2)
    if obs_id == 1
        observables = strcat(observables, obs_keys{1, obs_id}, '(', config.(obs_keys{1, obs_id}), ')');
    else
        observables = strcat(observables, '_', obs_keys{1, obs_id}, '(', config.(obs_keys{1, obs_id}), ')');
    end
end
attributes = sprintf('observables(%s)_cells(%s)', ... 
    observables, ...
    config.cells);

if strcmp(config.data_type, 'gene') || strcmp(config.data_type, 'bop') || strcmp(config.data_type, 'cpg') 
    fn = sprintf('%s/%s/%s/%s/%s/%s/%s', ...
                    config.data_base, ...
                    config.data_type, ...
                    config.experiment,...
                    config.task,...
                    config.method,...
                    annotations,...
                    attributes);
elseif strcmp(config.data_type, 'attributes')
    fn = sprintf('%s/%s', ...
                    config.data_base, ...
                    config.data_type);
end
end